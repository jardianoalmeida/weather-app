import 'weather_datasource_test.mocks.dart';

@GenerateMocks([IHttpClient])
void main() {
  late IWeatherDatasource sut;
  late MockIHttpClient httpClientSpy;

  setUp(() {
    httpClientSpy = MockIHttpClient();

    sut = WeatherDatasource(httpClientSpy);
  });

  PostExpectation mockHttpCall() => when(
        httpClientSpy.get(
          'forecast.json',
          query: {
            'days': 3,
            'aqi': 'yes',
            'lang': 'pt',
            'alerts': 'no',
            'q': '',
            'key': '5edf1a09a41f4b068f7152606220609',
          },
        ),
      );

  mockHttpSuccess(Map<String, dynamic> map) {
    mockHttpCall().thenAnswer((_) async => HttpResponse.success(map));
  }

  mockHttpFailure(exception) {
    mockHttpCall().thenThrow(exception);
  }

  group('IWeatherDatasource - Datasource / ', () {
    test('Should call datasource successfully', () async {
      // arrange
      mockHttpSuccess(mock);

      // act
      final result = await sut.getWeather('');
      // assert
      expect(result, isA<WeatherModel>());
    });

    test('Should throw ServerException if request fails', () async {
      // arrange
      mockHttpFailure(const BadRequestException());
      // act
      final result = sut.getWeather('');
      // assert
      expectException(result, const BadRequestException());
    });
  });
}

